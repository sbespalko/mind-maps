      &#160;&#160;&#160;&#160;For every field f tested in the equals() method, calculate a hash code c by:
    </p>
    <p>
      &#160;&#160;&#160;&#160;&#160;&#160;&#160;&#160;If the field f is a boolean: calculate (f ? 0 : 1);
    </p>
    <p>
      &#160;&#160;&#160;&#160;&#160;&#160;&#160;&#160;If the field f is a byte, char, short or int: calculate (int)f;
    </p>
